def seed_defaults(session) -> None:
    existing_sources = {source.domain: source for source in session.scalars(select(Source)).all()}
    direct_source_disabled_domains = {"86pla.com", "newsroom.haitian.com"}
    for payload in DEFAULT_SOURCES:
        existing = existing_sources.get(payload["domain"])
        if existing is None:
            session.add(Source(**payload))
            continue
        for field, value in payload.items():
            current = getattr(existing, field)
            if field in {"must_include_any", "must_exclude_any", "soft_signals"} and not current:
                setattr(existing, field, value)
            elif field == "source_tier" and (not current or current == "unknown"):
                setattr(existing, field, value)
            elif field == "rss_or_listing_url" and not current and value:
                setattr(existing, field, value)
            elif field == "use_direct_source" and not current and value:
                setattr(existing, field, value)
            elif field == "crawl_mode" and current == "search" and value != "search":
                setattr(existing, field, value)
        if existing.domain in direct_source_disabled_domains:
            existing.use_direct_source = False

    for key, value in DEFAULT_SETTINGS.items():
        setting = session.get(AppSetting, key)
        if setting is None:
            session.add(AppSetting(key=key, value=value))
        else:
            merged = {**value, **setting.value}
            if key == "report_settings":
                merged = {field: merged.get(field, default) for field, default in value.items()}
            if key == "report_settings" and merged.get("report_fallback_model") == "minimax/minimax-m2.5:free":
                merged["report_fallback_model"] = settings.report_fallback_model
            if merged != setting.value:
                setting.value = merged

    admin_exists = session.scalars(select(User).where(User.is_admin.is_(True))).first()
    if admin_exists is None:
        session.add(
            User(
                email=settings.admin_email,
                password_hash=hash_password(settings.admin_password),
                is_admin=True,
                is_active=True,
            )
        )
