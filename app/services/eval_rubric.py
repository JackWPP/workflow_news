WEIGHTS = {
    "faithfulness": 0.30,
    "coverage": 0.25,
    "dedup": 0.20,
    "fluency": 0.15,
    "research_value": 0.10,
}


def faithfulness_score_map(ratio: float) -> int:
    if ratio >= 0.95:
        return 5
    if ratio >= 0.85:
        return 4
    if ratio >= 0.70:
        return 3
    if ratio >= 0.50:
        return 2
    return 1


def compute_weighted_total(scores: dict[str, int]) -> float:
    return round(
        sum(scores.get(dim, 0) * WEIGHTS[dim] for dim in WEIGHTS),
        2,
    )
